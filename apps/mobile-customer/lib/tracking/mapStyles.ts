/**
 * Get map style based on preference
 */
export const getMapStyle = (theme: 'light' | 'dark' | 'tracking' = 'light') => {
  switch (theme) {
    case 'dark':
      return mapStylesDark;
    case 'tracking':
      return mapStylesTracking;
    case 'light':
    default:
      return mapStylesLight;
  }
};
